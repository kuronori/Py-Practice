import sys
def py2_or_py3():
    """
    実行中のPythonのバージョンを出力

    Returns
    -------
    None.

    """
    major=sys.version_info.major
    
    if major<3:
        return "Python2"
    else:
        return "Python3"

print(py2_or_py3())

